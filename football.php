                <h2>Перспектива есть</h2>
                <p>На ярмарке случилось странное происшествие: все наполнилось слухом, что где-то между товаром показалась красная свитка. Старухе, продававшей бублики, почудился сатана в образине свиньи, который беспрестанно наклонялся над возами, как будто искал чего. Это быстро разнеслось по всем углам уже утихнувшего табора; и все считали преступлением не верить... К этому присоединились еще увеличенные вести о чуде, виденном волостным писарем в развалившемся сарае, так что к ночи все теснее жались друг к другу; спокойствие разрушилось, и страх мешал всякому сомкнуть глаза свои; а те, которые были не совсем храброго десятка и запаслись ночлегами в избах, убрались домой. К числу последних принадлежал и Черевик с кумом и дочкою, которые вместе с напросившимися к ним в хату гостьми произвели сильный стук, так перепугавший нашу Хиврю. Кума уже немного поразобрало. Это можно было видеть из того, что он два раза проехал с своим возом по двору, покамест нашел хату. Гости тоже были в веселом расположении духа и без церемонии вошли прежде самого хозяина.</p>
